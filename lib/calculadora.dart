void main () {
  print(soma(2, 3));
  print(subtracao(5, 2));
  print(multiplicacao(4, 6));
  print(divisao(10, 2));
}

int soma (int a, int b) {
  return a + b;
}
int subtracao (int a, int b) {
  return a - b;
}
int multiplicacao (int a, int b) {
  return a * b;
}
double divisao (int a, int b) {
  return a / b;
}